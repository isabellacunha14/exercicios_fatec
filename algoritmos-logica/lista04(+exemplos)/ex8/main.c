#include <stdio.h>
//Um evento esportivo distribui números sequenciais aos corredores. Crie um
//programa que exiba na tela os números dos primeiros 30 corredores usando for

int main(void) {
    for (int i = 1; i<=30; i++){
        printf("O numero do corredor eh: %d \n", i);
    }
    return 0;
}
